import javax.persistence.Entity;
import javax.persistence.Id;

/**
 * @author grantjennings
 */
@Entity
public class InstanceWithNumericPrimaryKey {

	@Id
	private Long id;

	private String data;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}


}
